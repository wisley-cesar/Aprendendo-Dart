main() {
  // Operadores de atribuição (binário/ infix).
  double a = 2;
  a += 3;
  a -= 3;
  a *= 5;
  a /= 5;
  a %= 2;

  print(a);

  // Operadores Relacional (binário/ infix) --> o resultado sempre será BOLL

  print(3 > 2);
  print(3 >= 2);
  print(3 < 2);
  print(3 != 3);
  print(3 == 3);
  print(3 == '3');

  print(4 + 7 + 5 > 10 && 5 + 7 + 9 != 5 - 7 - 9);

  // 101 = 5
  // 100 = 4
  // 100 = 4
  print(5 & 4);
}
